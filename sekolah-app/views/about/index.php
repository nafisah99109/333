  <h2>Profile</h2>
			<p>SMA Negeri Semarang didirikan pada tahun 1985 sebagai salah satu upaya pemerintah untuk memperluas akses pendidikan bagi masyarakat di wilayah Kecamatan Sale dan sekitarnya. Sejak berdirinya, sekolah ini telah mengalami banyak perkembangan baik dari segi sarana prasarana maupun prestasi akademik dan non-akademik..</p>
<p>Visi dari SMA Negeri semarang adalah Terwujudnya peserta didik yang berkarakter, berprestasi, dan peduli lingkungan, dan Misi SMA Negeri Semarang antara lain:<br>
1.Menanamkan nilai-nilai keimanan dan ketakwaan kepada Tuhan Yang Maha Esa.<br>
2. Mengembangkan proses pembelajaran yang kreatif, inovatif, dan berpusat pada peserta didik.<br>
3.Mendorong dan memfasilitasi siswa untuk meraih prestasi akademik maupun non-akademik.</p>
<p>Pada tahun 2003 SMA Negeri Semarang  menambah 1 Akademi yaitu Agama.</p>
